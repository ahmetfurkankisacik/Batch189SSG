package ssg_dersler.ifStatements03;

public class C01_ifStatements {
    public static void main(String[] args) {
         /*
         <,>,==,>=,<=,!=
         */
        int sayi1=25;
        int sayi2=25;

        if (sayi1>sayi2){
            System.out.println("buyuk olan sayi : "+sayi1);
        }
        if (sayi2>sayi1){
            System.out.println("buyuk olan sayi : "+sayi2);
        }
        if (sayi2==sayi1){
            System.out.println("sayilar birbirine esit : "+sayi1);
        }




    }
}
